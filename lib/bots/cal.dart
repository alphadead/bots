import 'dart:math';

class Calculate {
  int add(int a, int b) {
    return a + b;
  }

  int substract(var a, var b) {
    return a - b;
  }

  int multiply(var a, var b) {
    return a * b;
  }

  double divide(var a, var b) {
    return a / b;
  }

  num expo(var a, var b) {
    return pow(a, b);
    // return a;
  }
}
